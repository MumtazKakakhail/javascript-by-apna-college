// Comparison operators 
let age = 22;
console.log(age > 18);
console.log(age >= 22);
console.log(age < 23);
console.log(age <= 22);


// equal to  == not strickly check
console.log(5 == 5);
console.log(5 == '5'); //true

// === strickly check
console.log(5 === '5'); //false

// not equal to !=
console.log(5 != 5); //false
console.log(5 != 4); //true 



