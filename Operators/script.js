// Arithmetic operators
let a = 25;
let b = 5;
console.log(a + b);
console.log(a - b);
console.log(a / b);
console.log(a * b);
console.log(a ** b);
console.log(a % b);

// Unary operators
console.log(a++);
console.log(++a);
console.log(b--);
console.log(--b);


// Assignment operators 
c = 30;
